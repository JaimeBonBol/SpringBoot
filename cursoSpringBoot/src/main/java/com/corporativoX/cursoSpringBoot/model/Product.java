package com.corporativoX.cursoSpringBoot.model;

public class Product {

    // Atributos. Cunado utilizamos un framework, usar tipos de datos referenciales en lugar de primitivos es considerado buenas pr√°cticas
    private Integer id;
    private String name;
    private Double price;
    private Integer stock;

    public Product(Integer id, String name, Double price, Integer stock) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.stock = stock;
    }

    public Product() {
    }

    // Getters y Setters

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public Integer getStock() {
        return stock;
    }

    public void setStock(Integer stock) {
        this.stock = stock;
    }


}
